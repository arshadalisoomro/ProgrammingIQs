package ali.arshad.db.model;

public class Subject {
	private String subjectId = null;
	private String subjectName = null;
	private String subjectPosition = null;
	private String subjectVisible = null;

	public String getSubjectId() {
		return subjectId;
	}

	public void setSubjectId(String subjectId) {
		this.subjectId = subjectId;
	}

	public String getSubjectName() {
		return subjectName;
	}

	public void setSubjectName(String subjectName) {
		this.subjectName = subjectName;
	}

	public String getSubjectPosition() {
		return subjectPosition;
	}

	public void setSubjectPosition(String subjectPosition) {
		this.subjectPosition = subjectPosition;
	}

	public String getSubjectVisible() {
		return subjectVisible;
	}

	public void setSubjectVisible(String subjectVisible) {
		this.subjectVisible = subjectVisible;
	}
}
